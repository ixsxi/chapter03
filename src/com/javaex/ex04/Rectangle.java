package com.javaex.ex04;

public class Rectangle {

	int width;
	int height;
	
	
	
	public Rectangle(int width, int height) {
	
		this.width = width;
		this.height = height;
	}



	@Override
	public String toString() {
		return "Rectangle [width=" + width + ", height=" + height + "]";
	}



	
	
	
	
	
	
	
	
	
}
